// String
// Text / Plain text => "kalimat disini"
console.log("Hello World!");

// Integer
// Bilangan bulat => 1 , 2, 3, 4
console.log(1, 2, 3, 4 + 6);
console.log(2);
console.log(123423442);

// Double / Float
// Bilangan desimal 1.0, 2.3, 55.2
console.log(2.3);
console.log(100.4);

// Boolean
// True / False => true, false
console.log(true);
console.log(false);

// Array
// Kumpulan dari banyak value => ["a", "b", "c", 30, 3.5, true, false]
console.log(["a", "b", "c", 30, 3.5, true, false])
